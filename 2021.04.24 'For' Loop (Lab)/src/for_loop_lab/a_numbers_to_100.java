package for_loop_lab;

public class a_numbers_to_100 
{
	public static void main(String[] args) 
	{
		System.out.println("Here are all of the numbers from 1 to 100!");
		
		for(int number=1; number<=100; number++) // начална стойност; крайна стойност; начин на промяна
			System.out.println(number);
	}
}
